    private void DepositSearchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DepositSearchButtonActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3307/bank","root","");
            Statement st= conn.createStatement();
            ResultSet rs= st.executeQuery("select * from balances where Name='"+id+"'");
            if(rs.next()){
            jTextField2.setText(rs.getString(1));
            jTextField3.setText(rs.getString(2));
            jTextField4.setText(rs.getString(4));
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Data doesn't exist");
            }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
    }//GEN-LAST:event_DepositSearchButtonActionPerformed

